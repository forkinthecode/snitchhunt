<details><summary><h5>About</h5></summary>

<p>SnitchHunt is a game designed by infosec consultant, Gabor Szathmari to teach the public about how different sets
of metadata can be brought together to identify track the movements of individuals</p>

<p>Rosie Williams, an open data and privacy advocate has extended Gabor's work through 
	creating a version of this game to run on an inexpensive server (ie not requiring Kibana), creating an additional game which is both shorter and easier to enable more people to get the benefit of the concept and added
	an introduction about metdata and the Data Retention Scheme.</p>

